/****************************************************************//**
 * @brief   Initialize communications
 * 
 * @param   none
 * @return  ESP_OK on success or ESP_ERR_[ERROR] otherwise
 *******************************************************************/
esp_err_t comm_init(void)
{

    /***************************************************************/
    // init BT
    /***************************************************************/
    ESP_ERROR_LOG(bt_init());

    /***************************************************************/
    // init UART
    /***************************************************************/
    ESP_ERROR_LOG(uart_init());

    return ESP_OK;
}


